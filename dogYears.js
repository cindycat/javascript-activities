//My age as a human
let myAge = 28;

//This variable is your early years
let earlyYears = 2;

earlyYears = earlyYears * 10.5;

//This variable is your later years
let laterYears = myAge - 2;

//dog years
laterYears = laterYears * 4;

console.log('earlyYears: ' + earlyYears, '\n' + 'laterYears: ' + laterYears);

//This is your age in dog years
myAgeInDogYears = earlyYears + laterYears;

//Create a new string variable which will be all small letter due to the .toLowerCase() method
let myName = "Cindy".toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);






